var average = 18;

// structure de contrôle "if / else"
if (average >= 10) {
    console.log("moyenne atteinte");
} else {
    console.log("moyenne non atteinte");
}

// structure de contrôle "if / else if / else"
if (average >= 15) {
    console.log("méga bravo !");
} else if (average >= 10) {
    console.log("bravo !");
} else {
    console.log("encore un petit effort");
}
